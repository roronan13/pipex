#include "ft_printf.h"
#include "../libft/libft.h"

int	ft_printf_s(char *s)
{
	long unsigned int	i;
	int					a;

	i = 0;
	if (!s)
	{
		write(1, "(null)", 6);
		return (6);
	}
	while (s[i])
		write(1, &s[i++], 1);
	a = (int)ft_strlen(s);
	return (a);
}
